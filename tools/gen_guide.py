title('9. 常见问题 (FAQ)')

h3('Q: 数据准确吗？')
p('数据来自东方财富和新浪财经的公开接口。东方财富是国内最大的财经网站之一，数据质量有保障。程序会每秒实时拉取，但受网络延迟影响，可能有几秒到几十秒的滞后。不适用于高频交易。')

h3('Q: 为什么全市场扫描偶尔会失败？')
p('东方财富的全市场扫描接口在国内某些网络环境下需要代理才能稳定访问。当东方财富不通时，程序会自动切到新浪财经。如果两个都失败，请检查网络连接或稍后再试。自选股行情不受影响。')

h3('Q: 程序会消耗很多流量吗？')
p('每次运行大约消耗 1-3MB 流量（拉取全市场数据和 K 线）。如果设置了定时推送（一天一次），一个月不到 100MB。')

h3('Q: 怎么卸载？')
p('直接删除 EXE 文件和它生成的 stock_history.db 即可。没有注册表、没有系统文件、没有任何残留。')

h3('Q: 能把数据导入到 Excel 吗？')
p('当前版本不支持导出。数据存在 stock_history.db（SQLite 数据库）中，可以用任何 SQLite 工具打开查看。')

h3('Q: 程序崩溃了怎么办？')
p('1. 删除 stock_history.db（缓存损坏可能导致启动问题）')
p('2. 删除 config.json（配置损坏会重置为默认）')
p('3. 重新启动程序')
p('如果仍然崩溃，请在 GitHub Issues 中报告，附上错误信息。')

h3('Q: 我可以在多台电脑上用同一个配置吗？')
p('把 config.json 复制到另一台电脑的相同目录即可。注意：通知通道的 Key/Webhook 会被复制过去，注意保护隐私。')

h3('Q: 为什么程序叫"大A雷达"？')
p('"哨兵"的含义是替你站岗放哨——你不需要一直盯着行情，程序会按你的条件去市场上"巡逻"，找到符合条件的票就通知你。类比军事里的哨兵站岗，发现敌情就吹哨子。')

